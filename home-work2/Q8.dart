//Q8-Write a Dart program that declares two numeric variables and performs addition, subtraction,
//multiplication, division, and modulo on them. Print each result.
void main() {
  num value1 = 20;
  num value2 = 10;
  num result1 = value1 + value2;
  print(result1);
  num result2 = value1 - value2;
  print(result2);
  num result3 = value1 * value2;
  print(result3);
  num result4 = value1 ~/ value2;
  print(result4);
  num result5 = value1 % value2;
  print(result5);
}